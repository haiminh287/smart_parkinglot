        public static List<SlotData> GenerateMockSlots()
        {
            var slots = new List<SlotData>();

            // Floor 0 (B1): V1-01..V1-72 car slots
            for (int i = 1; i <= 72; i++)
            {
                string status;
                if (i <= 18)       status = "reserved";
                else if (i <= 36)  status = (i % 3 == 0) ? "reserved" : "available";
                else if (i <= 54)  status = (i >= 46) ? "occupied" : "available";
                else               status = (i <= 70) ? "occupied" : "available";
                slots.Add(new SlotData
                {
                    Id = $"00000000-0000-0000-0010-0000000000{i:D2}",
                    Zone = MockIds.ZONE_CAR_PAINTED_F1,
                    Code = $"V1-{i:D2}",
                    Status = status,
                    IsAvailable = status == "available",
                    Camera = null,
                    X1 = 0, Y1 = 0, X2 = 100, Y2 = 200
                });
            }

            // Floor 0 (B1): V2-01..V2-20 motorbike slots
            for (int i = 1; i <= 20; i++)
            {
                string status = PickStatus(i, 20);
                slots.Add(new SlotData
                {
                    Id = $"00000000-0000-0000-0012-0000000000{i:D2}",
                    Zone = MockIds.ZONE_MOTO_F1,
                    Code = $"V2-{i:D2}",
                    Status = status,
                    IsAvailable = status == "available",
                    Camera = null,
                    X1 = 0, Y1 = 600, X2 = 100, Y2 = 700
                });
            }

            // Floor 1: A-01..A-18 (Zone A), B-01..B-18 (Zone B)
            for (int i = 1; i <= 18; i++)
            {
                string statusA = (i == 2 || i == 10) ? "occupied" : (i == 1 ? "reserved" : "available");
                slots.Add(new SlotData
                {
                    Id = $"00000000-0000-0000-0013-0000000000{i:D2}",
                    Zone = MockIds.ZONE_CAR_PAINTED_F2,
                    Code = $"A-{i:D2}",
                    Status = statusA,
                    IsAvailable = statusA == "available",
                    Camera = null,
                    X1 = 0, Y1 = 0, X2 = 100, Y2 = 200
                });
                string statusB = (i <= 10) ? "reserved" : "available";
                slots.Add(new SlotData
                {
                    Id = $"00000000-0000-0000-0014-0000000000{i:D2}",
                    Zone = MockIds.ZONE_CAR_PAINTED_F2,
                    Code = $"B-{i:D2}",
                    Status = statusB,
                    IsAvailable = statusB == "available",
                    Camera = null,
                    X1 = 0, Y1 = 0, X2 = 100, Y2 = 200
                });
            }

            // Floor 1: C-01..C-20 motorbike
            for (int i = 1; i <= 20; i++)
            {
                string status = PickStatus(i, 20);
                slots.Add(new SlotData
                {
                    Id = $"00000000-0000-0000-0015-0000000000{i:D2}",
                    Zone = MockIds.ZONE_MOTO_F2,
                    Code = $"C-{i:D2}",
                    Status = status,
                    IsAvailable = status == "available",
                    Camera = null,
                    X1 = 0, Y1 = 600, X2 = 100, Y2 = 700
                });
            }

            // Garage G-01..G-10 (not in DB, but generated by Unity)
            for (int i = 1; i <= 10; i++)
            {
                slots.Add(new SlotData
                {
                    Id = $"00000000-0000-0000-0011-0000000000{i:D2}",
                    Zone = MockIds.ZONE_CAR_GARAGE_F1,
                    Code = $"G-{i:D2}",
                    Status = "available",
                    IsAvailable = true,
                    Camera = null,
                    X1 = 0, Y1 = 300, X2 = 100, Y2 = 500
                });
            }

            return slots;
        }

