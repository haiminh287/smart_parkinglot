        private static BookingData MakeBooking(
            string bookingId, string vehicleId, string plate, string vType,
            string zoneId, string zoneName, string zoneVType, int cap, int avail,
            string slotId, string slotCode, BookingFloorInfo floor, BookingLotInfo lot,
            string startHour, string endHour, string checkInStatus, string price,
            string checkedInAt, string createdAt)
        {
            return new BookingData
            {
                Id = bookingId, UserId = MockIds.USER_1,
                Vehicle = new BookingVehicleInfo { Id = vehicleId, LicensePlate = plate, VehicleType = vType, Name = plate },
