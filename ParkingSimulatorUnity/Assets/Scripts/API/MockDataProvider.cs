        // ~60% available, ~20% occupied, ~15% reserved, ~5% maintenance
        private static string PickStatus(int index, int total)
        {
            float ratio = (float)index / total;
            if (ratio < 0.60f) return "available";
            if (ratio < 0.80f) return "occupied";
            if (ratio < 0.95f) return "reserved";
            return "maintenance";
        }
